package mx.itesm.externos.model;

public class Log {
	
	private String codigo_campus;
	private String institucion;
	private String clave_usuario;
	private String tipo_de_moviento;
	private String evidencia;
	private String estatus_operacion;
	private String fecha_movimiento;
	
	public String getCodigo_campus() {
		return codigo_campus;
	}
	public void setCodigo_campus(String codigo_campus) {
		this.codigo_campus = codigo_campus;
	}
	public String getInstitucion() {
		return institucion;
	}
	public void setInstitucion(String institucion) {
		this.institucion = institucion;
	}
	public String getClave_usuario() {
		return clave_usuario;
	}
	public void setClave_usuario(String clave_usuario) {
		this.clave_usuario = clave_usuario;
	}
	public String getTipo_de_moviento() {
		return tipo_de_moviento;
	}
	public void setTipo_de_moviento(String tipo_de_moviento) {
		this.tipo_de_moviento = tipo_de_moviento;
	}
	public String getEstatus_operacion() {
		return estatus_operacion;
	}
	public void setEstatus_operacion(String estatus_operacion) {
		this.estatus_operacion = estatus_operacion;
	}
	public String getFecha_movimiento() {
		return fecha_movimiento;
	}
	public void setFecha_movimiento(String fecha_movimiento) {
		this.fecha_movimiento = fecha_movimiento;
	}
	public String getEvidencia() {
		return evidencia;
	}
	public void setEvidencia(String evidencia) {
		this.evidencia = evidencia;
	}
	
	
	

}
