package mx.itesm.externos.model;

public class PersonasFisicas {

	
	private String id;
	private String nombre;
	private String a_paterno;
	private String a_materno;
	private String f_nacimiento;
	private String genero;
	private String edo_civil;
	private String nacionalidad;
	private String lugar_nacimiento;
	private String curp;
	private String rfc;
	private String institucion;
	private String codigo_campus;
	
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getA_paterno() {
		return a_paterno;
	}
	public void setA_paterno(String a_paterno) {
		this.a_paterno = a_paterno;
	}
	public String getA_materno() {
		return a_materno;
	}
	public void setA_materno(String a_materno) {
		this.a_materno = a_materno;
	}
	public String getF_nacimiento() {
		return f_nacimiento;
	}
	public void setF_nacimiento(String f_nacimiento) {
		this.f_nacimiento = f_nacimiento;
	}
	public String getGenero() {
		return genero;
	}
	public void setGenero(String genero) {
		this.genero = genero;
	}
	public String getEdo_civil() {
		return edo_civil;
	}
	public void setEdo_civil(String edo_civil) {
		this.edo_civil = edo_civil;
	}
	public String getNacionalidad() {
		return nacionalidad;
	}
	public void setNacionalidad(String nacionalidad) {
		this.nacionalidad = nacionalidad;
	}
	public String getLugar_nacimiento() {
		return lugar_nacimiento;
	}
	public void setLugar_nacimiento(String lugar_nacimiento) {
		this.lugar_nacimiento = lugar_nacimiento;
	}
	public String getCurp() {
		return curp;
	}
	public void setCurp(String curp) {
		this.curp = curp;
	}
	public String getRfc() {
		return rfc;
	}
	public void setRfc(String rfc) {
		this.rfc = rfc;
	}
	public String getInstitucion() {
		return institucion;
	}
	public void setInstitucion(String institucion) {
		this.institucion = institucion;
	}
	public String getCodigo_campus() {
		return codigo_campus;
	}
	public void setCodigo_campus(String codigo_campus) {
		this.codigo_campus = codigo_campus;
	}
	
	
}
