package mx.itesm.externos.model;

public class Sesion {
	
	private String codigo_campus;
	private String institucion;
	private String clava_usuario;
	private Integer n_acceso;
	private String informacion_cliente;
	private String f_fecha_ultima_sesion;
	private String f_fecha_de_movimiento;
	
	
	public String getCodigo_campus() {
		return codigo_campus;
	}
	public void setCodigo_campus(String codigo_campus) {
		this.codigo_campus = codigo_campus;
	}
	public String getInstitucion() {
		return institucion;
	}
	public void setInstitucion(String institucion) {
		this.institucion = institucion;
	}
	public String getClava_usuario() {
		return clava_usuario;
	}
	public void setClava_usuario(String clava_usuario) {
		this.clava_usuario = clava_usuario;
	}
	public Integer getN_acceso() {
		return n_acceso;
	}
	public void setN_acceso(Integer n_acceso) {
		this.n_acceso = n_acceso;
	}
	public String getInformacion_cliente() {
		return informacion_cliente;
	}
	public void setInformacion_cliente(String informacion_cliente) {
		this.informacion_cliente = informacion_cliente;
	}
	public String getF_fecha_ultima_sesion() {
		return f_fecha_ultima_sesion;
	}
	public void setF_fecha_ultima_sesion(String f_fecha_ultima_sesion) {
		this.f_fecha_ultima_sesion = f_fecha_ultima_sesion;
	}
	public String getF_fecha_de_movimiento() {
		return f_fecha_de_movimiento;
	}
	public void setF_fecha_de_movimiento(String f_fecha_de_movimiento) {
		this.f_fecha_de_movimiento = f_fecha_de_movimiento;
	}
	
	
	
	
		

}
