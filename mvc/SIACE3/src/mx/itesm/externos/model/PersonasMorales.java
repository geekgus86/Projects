package mx.itesm.externos.model;

public class PersonasMorales {
	
	private String id;
	private String nombre;
	private String r_comercial;
	private String rfc;
	private String r_social;
	private String institucion;
	private String codigo_campus;
		
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getR_comercial() {
		return r_comercial;
	}
	public void setR_comercial(String r_comercial) {
		this.r_comercial = r_comercial;
	}
	public String getRfc() {
		return rfc;
	}
	public void setRfc(String rfc) {
		this.rfc = rfc;
	}
	public String getR_social() {
		return r_social;
	}
	public void setR_social(String r_social) {
		this.r_social = r_social;
	}
	public String getInstitucion() {
		return institucion;
	}
	public void setInstitucion(String institucion) {
		this.institucion = institucion;
	}
	public String getCodigo_campus() {
		return codigo_campus;
	}
	public void setCodigo_campus(String codigo_campus) {
		this.codigo_campus = codigo_campus;
	}
	
	
	
	

}
